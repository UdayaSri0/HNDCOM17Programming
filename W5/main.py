# Assign values to variables
x = 10
y = 3

# Print the values of x and y
print("x =", x)
print("y =", y)

# Addition
print("x + y =", x + y)

# Subtraction
print("x - y =", x - y)

# Multiplication
print("x * y =", x * y)

# Division (returns float)
print("x / y =", x / y)

# Modulus (remainder)
print("x % y =", x % y)

# Floor division (returns integer)
print("x // y =", x // y)

# Exponentiation (power)
print("x ** y =", x ** y)

# Addition assignment operator (x = x + y)
x += y
print("After x+=y, now x =", x)

# Subtraction assignment operator (x = x - y)
x -= y
print("After x-=y, now x =", x)

# Multiplication assignment operator (x = x * y)
x *= y
print("After x*=y, now x =", x)

# Division assignment operator (x = x / y)
x /= y
print("After x/=y, now x =", x)

